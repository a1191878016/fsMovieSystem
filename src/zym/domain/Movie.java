package zym.domain;

public class Movie {
	private int MovieId;
	private String MovieName;
	private String MovieActor;
	private String MovieDirector;
	private String MovieDate;
	private String MovieType;
	private int IsGoing;
	private String MoviePhoto;
	private String MovieInformation;
	private int MovieTime;
	private String Country;
	public int getMovieId() {
		return MovieId;
	}
	public void setMovieId(int movieId) {
		MovieId = movieId;
	}
	public String getMovieName() {
		return MovieName;
	}
	public void setMovieName(String movieName) {
		MovieName = movieName;
	}
	public String getMovieActor() {
		return MovieActor;
	}
	public void setMovieActor(String movieActor) {
		MovieActor = movieActor;
	}
	public String getMovieDirector() {
		return MovieDirector;
	}
	public void setMovieDirector(String movieDirector) {
		MovieDirector = movieDirector;
	}
	public String getMovieDate() {
		return MovieDate;
	}
	public void setMovieDate(String movieDate) {
		MovieDate = movieDate;
	}
	public String getMovieType() {
		return MovieType;
	}
	public void setMovieType(String movieType) {
		MovieType = movieType;
	}
	public int getIsGoing() {
		return IsGoing;
	}
	public void setIsGoing(int isGoing) {
		IsGoing = isGoing;
	}
	public String getMoviePhoto() {
		return MoviePhoto;
	}
	public void setMoviePhoto(String moviePhoto) {
		MoviePhoto = moviePhoto;
	}
	public String getMovieInformation() {
		return MovieInformation;
	}
	public void setMovieInformation(String movieInformation) {
		MovieInformation = movieInformation;
	}
	public int getMovieTime() {
		return MovieTime;
	}
	public void setMovieTime(int movieTime) {
		MovieTime = movieTime;
	}
	public String getCountry() {
		return Country;
	}
	public void setCountry(String country) {
		Country = country;
	}
	
}
